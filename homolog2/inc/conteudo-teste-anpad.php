			<h3>Sobre o Teste ANPAD</h3>
            <p>O Teste ANPAD, criado em 1987 pela Associação Nacional de Pós-Graduação e Pesquisa em Administração, é um exame nacional que avalia: conhecimentos das línguas portuguesa e inglesa; e habilidades em raciocínios lógico, quantitativo e analítico.</p>

<p>Esse exame tem sido utilizado por diversas instituições como parte dos processos de seleção de cursos de pós-graduação stricto sensu e de cursos profissionalizantes de Administração, Ciências Contábeis e áreas afins, além de requisito básico em processos seletivos de diversas organizações.</p>

<p><strong>Provas</strong></p>

<p>Compõe-se de 5 (cinco) provas, cada qual com 17 questões:<br><br>
- Inglês <br>
- Português <br>
- Raciocínio Analítico <br>
- Raciocínio Lógico <br>
- Raciocínio Quantitativo<br></p> 

