<p><strong>Inscrição</strong></p>
<p>A inscrição para o Teste ANPAD é realizada somente através do site www.anpad.org.br . A taxa de inscrição deve ser paga por meio de boleto bancário, impresso após preenchimento on-line do formulário de inscrição. </p>
<p>O processo de inscrição nas diversas instituições usuárias do Teste é independente do processo de inscrição no Teste ANPAD. Informações específicas sobre o processo de seleção das instituições usuárias do Teste devem ser obtidas junto às secretarias ou departamentos responsáveis de cada um dos cursos ou organizações de interesse.</p>

<p><strong>Edições e Período de validade</strong></p>
<p>O Teste ANPAD é realizado em três edições anuais, geralmente nos meses de fevereiro, junho e setembro. Cada interessado pode submeter-se ao Teste ANPAD quantas vezes desejar, desde que efetue o pagamento da taxa de inscrição correspondente a cada edição.</p>
<p>O resultado obtido no Teste ANPAD tem validade de, no máximo, dois anos (período correspondente às seis últimas edições). Porém, cada instituição usuária pode definir as edições que deseja utilizar. Nos casos em que (i) uma instituição usuária valide mais de uma edição para seu processo seletivo e (ii) um inscrito possua mais de um resultado válido, a apuração considera apenas aquela edição em que o inscrito obteve maior resultado geral.</p>

<p><strong>Certificado</strong></p>
<p>Os inscritos que realizam o Teste ANPAD recebem, via de regra, um certificado relativo ao seu desempenho nas provas. Os resultados obtidos pelos inscritos são também automaticamente disponibilizados às instituições usuárias selecionadas no ato da inscrição. </p>

<p><strong>Materiais para estudo</strong></p>
<p>A ANPAD não mantém qualquer vínculo nem convênio com cursos preparatórios para o Teste ANPAD. Com o objetivo de apoiar os inscritos nos estudos, a Coordenação do Teste ANPAD disponibiliza o Caderno Teste ANPAD e kits de provas avulsas de edições anteriores. Para obtenção desses materiais, consulte a seção Preparação no site www.anpad.org.br.</p>
