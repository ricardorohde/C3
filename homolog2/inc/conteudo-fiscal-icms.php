			<h3>Informações sobre a Carreira</h3>
            <p>A Carreira Fiscal é uma das mais cobiçadas, pois oferece significativas remunerações e condições de trabalho. Podemos dividi-la em três categorias: fiscal municipal, estadual e federal.</p>

<p>O Auditor-Fiscal é a autoridade pública responsável pela Administração Tributária e Aduaneira ou pela fiscalização do cumprimento das legislações trabalhistas. Todo o seu trabalho deve ser embasado no Princípio da Legalidade. O lançamento do crédito tributário, uma das principais responsabilidades do Auditor-Fiscal, ou seja, a instituição do débito do contribuinte é de sua atribuição exclusiva.</p>

<p>Existem Auditores-Fiscais Federais (da Receita Federal e do Trabalho), Estaduais e Municipais Dentro e fora do município, e com as mais diversas denominações, mas todos responsáveis pela Administração Tributária do seu Ente Federado.</p>

<p>Sua atuação está prevista pela Constituição Federal, pelo Código Tributário Nacional, pela Consolidação das Leis do Trabalho e demais leis federais, as leis estaduais e municipais. </p>

<p>A auditoria tributária objetiva o exame e a avaliação de planejamento tributário e a eficiência e eficácia dos procedimentos e controles adotados para a operação, pagamento e recuperação de impostos, taxas e quaisquer ônus de natureza fisco-tributário que incida nas operações, bens e documentos da empresa".</p>

<p>Ela só pode ser realizada por servidor público empregado no ente tributante. Conforme artigo 142 do Código Tributário Nacional: "Compete privativamente a autoridade administrativa constituir o crédito tributário pelo lançamento, assim entendido o procedimento administrativo tendente a verificar a ocorrência do fato gerador da obrigação correspondente, determinar a matéria tributável, calcular o montante do tributo devido, identificar o sujeito passivo e, sendo o caso, propor aplicação da penalidade cabível".</p>

<p>A Receita Federal ocupa o topo da preferência, mas há concursos para todos os estados – conhecidos como concursos para ICMS – e, ainda, os concursos municipais – mais conhecidos como concursos para ISS. Assim, as oportunidades são inúmeras e a preparação deve ser adequada para deixar o candidato habilitado a concorrer a todas elas.</p>

<p>O curso C3 é o único preparatório do Rio de janeiro que prepara o aluno com todas as disciplinas dos últimos concursos, sejam elas na área federal, estadual ou municipal.</p>
