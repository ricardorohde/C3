			<h3>Informações sobre a Carreira</h3>
			<p>
				<strong class="tit">Sobre o oficial de chancelaria</strong>
                OFICIAL DE CHANCELARIA é a carreira responsável pela análise técnica e gestão administrativa necessárias ao desenvolvimento da política externa brasileira. É uma das duas carreiras de nível superior do Ministério das Relações Exteriores do Brasil (MRE) e foi criada pela Lei nº 8.829, de 22 de dezembro de 1993, como parte integrante do Serviço Exterior Brasileiro.<br /><br />
Segundo a Lei nº 11.440, de 29 de dezembro de 2006, o Serviço Exterior Brasileiro (SEB), essencial à execução da política exterior do Brasil, constitui-se do corpo de servidores, ocupantes de cargos de provimento efetivo, capacitados profissionalmente como agentes do Ministério das Relações Exteriores, no Brasil e no exterior, organizados em carreiras: de Diplomata e de Oficial de Chancelaria, ambas de nível superior; e de Assistente de Chancelaria, de nível médio.<br /><br />
<strong>ATRIBUIÇÕES DO OFICIAL DE CHANCELARIA</strong><br /><br />
Os servidores integrantes da carreira de Oficial de Chancelaria são analistas e gestores do Serviço Exterior Brasileiro, de nível superior, a quem incumbem, com exclusividade, "atividades de formulação, implementação e execução dos atos de análise técnica e gestão administrativa necessários ao desenvolvimento da política externa brasileira", nos termos do art. 4º da Lei nº 11.440/2006.<br /><br />
A carreira de Oficial de Chancelaria tem, assim, atribuições específicas e de competência privativa no Serviço Exterior Brasileiro, que não se confundem com aquelas das outras duas carreiras integrantes do Serviço Exterior Brasileiro, conforme expressamente dispõe a Lei nº 11.440/2006: aos servidores da carreira de Diplomata "incumbem atividades de natureza diplomática e consular, em seus aspectos específicos de representação, negociação, informação e proteção de interesses brasileiros no campo internacional" (art. 3º), enquanto aos servidores integrantes da carreira de Assistentes de Chancelaria, de nível médio, "incumbem tarefas de apoio técnico e administrativo" (art. 5º)<br /><br />
No desempenho de suas funções, o servidor integrante da carreira de Oficial de Chancelaria é, assim, responsável, no âmbito do Ministério das Relações Exteriores, pelo estrito cumprimento das normas e regulamentos aplicáveis à Administração Pública, como a Constituição Federal, a Lei de Licitações (Lei n. 8.666/1993), a Lei do Processo Administrativo Federal (Lei n. 9.784/1999), entre outros, e daqueles aplicáveis ao Ministério das Relações Exteriores e suas repartições no Brasil e no Exterior, especialmente o Manual do Serviço Consular e Jurídico, o Guia de Administração dos Postos, além de outras normas técnicas internas, como Portarias, Circulares e demais atos normativos e regulamentares.<br /><br />
Por ser carreira pluridisciplinar, formada por profissionais com perfil generalista, multicultural e de alta qualificação técnica, o Oficial de Chancelaria tem a oportunidade de praticar suas habilidades trazidas de experiências profissionais anteriores, quaisquer que sejam, desde direito, contabilidade e administração até jornalismo, entre outras, além de suas habilidades linguísticas, em razão da flexibilidade da carreira, que possibilita o desempenho das mais diversas funções, no Brasil ou no exterior.<br /><br />
Quando em exercício na Secretaria de Estado das Relações Exteriores, ou seja, na sede do Ministério das Relações Exteriores, em Brasília, o Oficial de Chancelaria pode ser lotado em qualquer dos seus órgãos. <br /><br />
É possível, também, a lotação nos Escritórios de Representação do Ministério das Relações Exteriores nas capitais de alguns Estados brasileiros.<br /><br />
Quando em exercício nas repartições no exterior, é frequente que o Oficial de Chancelaria seja designado para funções com grande sensibilidade política e necessidade de manutenção de sigilo, como a função de Vice-Cônsul e/ou a Chefia de Setor como Administração, Contabilidade, Comunicações, Processamento de Informação Comercial ou Consular.<br /><br />
<strong>CARREIRA TÍPICA DE ESTADO</strong><br /><br />
A carreira de Oficial de Chancelaria foi expressamente reconhecida como carreira típica de Estado quando da definição desse conceito, pelo Ministério da Administração Federal e Reforma do Estado (MARE), por ocasião da Reforma do Estado por este empreendida.<br /><br />
São carreiras típicas de Estado aquelas "cujas atividades estão voltadas para as atividades exclusivas de Estado relacionadas com a formulação, controle e avaliação de políticas públicas e com a realização de atividades que pressupõem o poder de Estado" ("o poder de regulamentar, fiscalizar, fomentar"), de cujos exemplos cita-se "o serviço de emissão de passaportes", entre outros. Neste sentido, apesar de não haver, no Brasil, um marco legal amplo que defina quais carreiras são típicas de Estado, diversos estudos indicam que a carreira de Oficial de Chancelaria detém "os qualificativos necessários à sua classificação como responsáveis por atividades exclusivas de Estado, inequivocamente"<br /><br />
De fato, com a sanção da Lei nº 12.775, de 28 de dezembro de 2012, a transformação da natureza da estrutura remuneratória em subsídio (art. 1º) e a aplicação, aos integrantes da carreira, do regime de dedicação exclusiva - com o impedimento do exercício de outra atividade remunerada, pública ou privada, potencialmente causadora de conflito de interesses, ressalvado o exercício do magistério, havendo compatibilidade de horários (art. 8º) -, consagram definitivamente o reconhecimento da carreira de Oficial de Chancelaria como carreira típica de Estado.<br /><br />
O processo de seleção para OFICIAL DE CHANCELARIA exige formação superior em qualquer área e ocorre por meio de concurso público, com prova objetiva e também exame escrito (redação em Inglês e Português).<br /><br />
O último concurso para Oficial de chancelaria foi realizado em 2008 e a expectativa para um novo concurso é muito grande, pois, além da criação das 400 vagas para diplomata, a lei 12.601/12 cria também 893 vagas de oficial de chancelaria. O salário inicial de um OFCHAN atualmente está em torno de R$ 6.000,00.<br /><br />
